  <h2>Общие сведения</h2>
  <p>ТПП Краснодарского края объединяет 32 межрайонные торгово-промышленные палаты
    и 6 их представительств — это 19 % от общего числа торгово-промышленных палат
    Российской Федерации. Палата объединяет более 5 200 предприятий и организаций;
    в разрезе общероссийских показателей доля членов ТПП Краснодарского края
    составляет 9,8 %.</p>
  <p>Палата сертифицирована на соответствие требованиям международного стандарта
    менеджмента качества ISO 9001:2015. По комплексу оказываемых услуг
    ТПП Краснодарского края входит в первую пятёрку лучших торгово-промышленных
    палат России.</p>

  <h2>История: от биржевого общества 1909 года</h2>
  <p>Предшественником палаты в начале XX века было Екатеринодарское биржевое общество.
    Первой на Кубани появилась Новороссийская биржа (1904), её учредили 16 крупнейших
    фирм города. Затем возникло Екатеринодарское биржевое общество, объединившее
    144 юридических и физических лица, а позже — торгово-промышленные биржи
    в Ейске и Армавире.</p>
  <p>Днём основания Екатеринодарского биржевого общества считается
    14 (28) января 1909 года: Городская Дума единогласно признала создание Торговой
    биржи «весьма желательным, соответствующим назревшим нуждам торговли».
    К сентябрю 1909 года был утверждён список членов биржевого общества и открылась
    контора Биржевого Комитета. Среди основателей — виднейшие купцы и промышленники
    Кубани: Аведовы, Богарсуковы, Дицманы, а также Азовско-Донской и Волжско-Камский
    коммерческие банки. Первым председателем был избран купеческий староста
    Андрей Евсеевич Филиппенко.</p>
  <p>В 1912 году при Биржевом Комитете была учреждена Кубано-Черноморская биржевая
    артель. Деятельность комитета продолжалась до марта 1920 года.</p>
  <p>Современная история началась в 1969 году с создания Краснодарского отделения
    Всесоюзной торговой палаты, которое за три десятилетия выросло в краевую систему
    торгово-промышленных палат.</p>

  <h2>Основные задачи и направления</h2>
  <p>Палата содействует созданию правовой базы рыночных отношений в крае и развитию
    деловой инициативы: представляет интересы предпринимателей независимо от форм
    собственности, участвует в экспертизе краевых законопроектов и программ,
    организует взаимодействие бизнеса с органами власти.</p>
  <p>Ключевые направления работы:</p>
  <ul>
    <li>товарная экспертиза и сертификация происхождения товаров;</li>
    <li>сертификация соответствия продукции, услуг розничной торговли и общественного питания;</li>
    <li>лабораторные испытания пищевой продукции;</li>
    <li>штриховое кодирование продукции;</li>
    <li>оценка стоимости всех видов собственности, бизнес-планирование;</li>
    <li>маркетинг и внешнеэкономическая деятельность;</li>
    <li>переводы, дизайнерские и издательские услуги;</li>
    <li>юридические услуги и деловое образование;</li>
    <li>выставки, презентации, собственные периодические издания.</li>
  </ul>
  <p>При палате действует 21 Центр поддержки и содействия развитию предпринимательства
    с «горячими линиями» для предпринимателей: бесплатные юридические консультации,
    представление интересов в судах, подготовка учредительных документов
    по принципу «единого окна».</p>

