  <p>Допускается пропорциональное <strong>увеличение до 200 %</strong> от номинала
    и <strong>уменьшение до 80 %</strong>:</p>
  <ul>
    <li>номинальный размер (со свободными зонами): 37,29 × 25,93 мм (100 %);</li>
    <li>минимальный: 29,83 × 20,74 мм (80 %);</li>
    <li>максимальный: 74,58 × 51,86 мм (200 %).</li>
  </ul>

  <div class="note"><strong>Усечение кода по высоте недопустимо.</strong>
    «Красивые» узкие коды-лесенки, у которых срезана высота при сохранении ширины,
    мешают работе многолучевых сканеров в супермаркетах. Не уменьшайте высоту символа
    более чем на 30 % — она не должна быть меньше 16 мм.</div>

