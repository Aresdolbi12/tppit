  <h2>Цвета и контраст</h2>
  <p>Оптимальное сочетание — <strong>чёрные штрихи на белом фоне</strong>.
    Если дизайн упаковки требует других цветов:</p>
  <ul>
    <li>для штрихов — только тёмные цвета: чёрный, тёмно-синий, тёмно-коричневый, тёмно-зелёный;</li>
    <li>для фона — светлые тона, допустимы «красноватые» оттенки: оранжевый, розовый, персиковый, жёлтый;</li>
    <li>большинство сканеров использует инфракрасный источник — красные штрихи для них невидимы.</li>
  </ul>

