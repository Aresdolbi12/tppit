  <h2>Свободные зоны</h2>
  <p>Перед первым штрихом и после последнего обязательны свободные зоны —
    любая печать внутри них мешает считыванию. Размер зон зависит от типа
    и масштаба кода.</p>

  <figure>
    <img src="/assets/img/svob_zon.png" alt="Таблица значений свободных зон для номинального размера EAN-13" loading="lazy">
