  <h2>Номинальные размеры EAN-13</h2>
  <ul>
    <li>ширина символа — 37,29 мм;</li>
    <li>высота символа — 25,93 мм;</li>
    <li>высота штриха — 22,85 мм;</li>
    <li>левая свободная зона — 3,63 мм (11X);</li>
    <li>правая свободная зона — 2,31 мм (7X);</li>
    <li>штрихи левого, правого и центрального знаков-ограничителей удлиняются вниз на 1,65 мм (5X);</li>
    <li>высота цифр — 2,75 мм; расстояние от цифр до штрихов — не менее 0,165 мм (0,5X).</li>
  </ul>

