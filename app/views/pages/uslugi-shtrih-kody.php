  <h2>Зачем нужен штрих-код</h2>
  <p>Торговые сети — «Магнит», «Ашан», «О'Кей», «Табрис» и другие — принимают товар
    только с нанесённым штрих-кодом: без него продукция не попадёт на полку.
    Код автоматизирует приёмку, учёт запасов и продажу товара, а при внешнеторговых
    сделках наличие штрих-кода на упаковке — обязательное условие экспорта.</p>

