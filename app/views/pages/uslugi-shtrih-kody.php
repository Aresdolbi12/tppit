  <h2>Какие коды мы регистрируем</h2>
  <ul>
    <li><strong>EAN-13</strong> — на единичную (потребительскую) упаковку: 13 цифр,
      тот самый код, который сканируют на кассе;</li>
    <li><strong>ITF-14</strong> — на групповую (транспортную) упаковку: 14 цифр,
      для коробов и паллет.</li>
  </ul>

  <p>Готовые макеты передаём в любом графическом формате — JPG, SVG, PNG, BMP и других —
